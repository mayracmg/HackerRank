#  1. INTEGER n
#  2. INTEGER_ARRAY ar
#

def sockMerchant(n, ar):
    total_pairs = 0
    pairs = {}
    for sock in ar:
        try:
            pairs[sock] += 1
            if (pairs[sock] % 2 == 0):
                total_pairs += 1
        except:
            pairs[sock] = 1

    return total_pairs

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    n = int(input().strip())

    ar = list(map(int, input().rstrip().split()))

    result = sockMerchant(n, ar)

    fptr.write(str(result) + '\n')

    fptr.close()
